            <div class="wwd-pg-space">
                In the years surrounding the World Wars, citizens in many countries affected by the wars 
                planted vegetables to help make up for food shortages. These "Victory Gardens," as they 
                were often called, found home on public land as their objective was a civic one that supported 
                both the war effort and the troops abroad. Despite their success and popularity,  after the 
                conclusion of World War II when the US has returned to prosperity and surplus, the original 
                community gardens mostly died out.
            </div>
            <div class="wwd-pg-space">
                Project Grow was founded by Susan Drake, a resident of the Burns Park neighborhood of Ann Arbor. 
                Susan wanted to start a “Victory Garden” for herself and some neighbors using public land. She 
                arranged to use some undeveloped land at Stone School and Ellsworth until it was sold for development. 
                Water for the site was drawn from a nearby creek. About one acre of land was available. This was more 
                space than the Burns Park gardeners needed, so Susan invited residents of Arbor Park (now University 
                Townhouses), a nearby low income housing development, to join them.  Altogether in 1971, 43 families 
                gardened together at the Stone School site, about half from Burns Park and half from Arbor Park. 
            </div>
            <div class="wwd-pg-space">
                The success of the first year’s garden convinced Susan Drake that community gardening should be available 
                city wide. And it was to be more than simply gardening. The joining together in a single effort of affluent 
                Burns Park residents and the low income and more diverse residents of Arbor Park was a model that should be replicated.
            </div>
